#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>

char	*ft_strrchr(const char *str, int schar)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (str[i] == (unsigned char)schar)
			return ((char *)&str[i]);
		i--;
	}
	return (NULL);
}
